function Usuarios(apellido, dni, domicilio, email, estado, fechaCreacion, fechaModificacion, fechaNacimineto, idUsuario, idUsuarioCreacion, idUsuarioModificacion, imagen, nombre, passwordUsuario, provincia, roles, telefono, username) {
    this.apellido = apellido;
    this.dni = dni;
    this.domicilio = domicilio;
    this.email = email;
    this.estado = estado;
    this.fechaCreacion = fechaCreacion;
    this.fechaModificacion = fechaModificacion;
    this.fechaNacimineto = fechaNacimineto;
    this.idUsuario = idUsuario;
    this.idUsuarioCreacion = idUsuarioCreacion;
    this.idUsuarioModificacion = idUsuarioModificacion;
    this.imagen = imagen;
    this.nombre = nombre;
    this.passwordUsuario = passwordUsuario;
    this.provincia = provincia;
    this.roles = roles;
    this.telefono = telefono;
    this.username = username;
}

